
def area(a, h):
    '''Принимает значения стороны треугольника и высоты, проведенной к ней,
       возвращает площадь треугольника.'''
    return a * h / 2


def perimeter(a, b, c):
    '''Принимает значения всех сторон треугольника,
       возвращает периметр треугольника.'''
    return a + b + c
